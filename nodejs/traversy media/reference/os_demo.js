const os = require('os');
//platform
console.log(os.platform());

// CPU arch
console.log(os.arch());

// CPU core info
console.log(os.cpus());

// Free Memory
console.log(os.freemem());


//Total memory
console.log(os.totalmem());

// Home dir
console.log(os.homedir());


// Up time
console.log(os.uptime());
